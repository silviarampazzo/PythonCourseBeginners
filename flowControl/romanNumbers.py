import random

x = random.randint(1, 10)

if x == 1:
    print("Roman number for ", str(x), " is I")
elif x == 2:
    print("Roman number for ", str(x), " is II")
elif x == 3:
    print("Roman number for ", str(x), " is III")
elif x == 4:
    print("Roman number for ", str(x), " is IV")
elif x == 5:
    print("Roman number for ", str(x), " is V")
elif x == 6:
    print("Roman number for ", str(x), " is VI")
elif x == 7:
    print("Roman number for ", str(x), " is VII")
elif x == 8:
    print("Roman number for ", str(x), " is VIII")
elif x == 9:
    print("Roman number for ", str(x), " is IX")
else:
    print("Roman number for ", str(x), " is X")
